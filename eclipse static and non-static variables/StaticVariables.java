package demo;

public class Static_Variable {
	static byte b1 ;
	static short s1 ;
	static int i1 ;
	static long L1 ;
	static float f1 ;
	static double d2 ;
	static char ch ;
	static boolean bool ;
	static String n1 ;
	public static void main(String[] args) {
		System.out.println("the value of b1: "+b1);
		System.out.println("the value of s1: "+s1);
		System.out.println("the value of i1: "+i1);
		System.out.println("the value of L1: "+L1);
		System.out.println("the value of f1: "+f1);
		System.out.println("the value of d2: "+d2);
		System.out.println("the value of ch: "+ch);
		System.out.println("the value of bool: "+bool);
		System.out.println("the value of String: "+ n1);
		
	}
}