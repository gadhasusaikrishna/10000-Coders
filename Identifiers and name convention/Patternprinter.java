public class Patternprinter {
    public static void main(String[] args) {
        System.out.println("\" \" \" \" \" \" \" \"");
        System.out.println("\"   *   *    \"");
        System.out.println("\"     *       \"");
        System.out.println("\"   * * *    \"");
        System.out.println("\" \" \" \" \" \" \" \"");
    }
}

//The backslash (\) before each double quote (") is an escape character. It ensures that Java treats the quotes as part of the string instead of ending the string.